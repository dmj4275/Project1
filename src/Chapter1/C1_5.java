package Chapter1;

/**
 * Program that finds the quotient of two sums and prints the result
 *
 * @author Dylan Jamison
 */
public class C1_5 {

    /**
     * Main Method
     *
     * @param args arguments from command line prompt
     */
    public static void main(String[] args) {
        double sum1 = 9.5 * 4.5 - 2.5 * 3;
        double sum2 = 45.5 - 3.5;
        double result = sum1 / sum2;
        System.out.println(result);
    }

}
